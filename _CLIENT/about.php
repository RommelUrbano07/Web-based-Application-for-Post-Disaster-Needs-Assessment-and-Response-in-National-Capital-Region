          <div class="col-md-5 d-flex align-items-center bg-black">
            <div class="about-text py-5 pl-md-5">
              <h2>What is iRespond<span id="ph">PH</span>?</h2>
              <p class="text-justify pr-4">
                IRespondPH, is a Post Disaster Needs Assessment tool. In an
                aftermath of a disaster, this application aims to provide
                crucial and real time information to users using HeatMap,
                pre-during-post disaster guides and tips, and reporting feature.
                Also, IRespondPH gives LGU authority access to a disaster
                database to monitor and assess the impact of the calamity -
                capable of updating, authenticating and scaling functionality.
                Overall, IRespondPH aims to provide assessment to improve the
                decision maker's ability to facilitate response and recovery
                efforts efficiently.
              </p>
