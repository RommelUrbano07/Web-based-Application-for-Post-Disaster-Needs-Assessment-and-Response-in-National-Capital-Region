                    What is Post Disaster Need Assessment?
                  </h2>
                  <p class="text-justify" id="pdna-text">
                    The <b>Post-Disaster Needs Assessment (PDNA)</b> is an
                    internationally accepted methodology for determining the
                    physical damages, economic losses, and costs of meeting
                    recovery needs after a natural disaster through a
                    government-led process. As of mid-2017, 55 PDNAs have been
                    conducted since the methodology was elaborated by the
                    European Union (EU), the United Nations Development
                    Programme (UNDP), and the World Bank (WB). A review of the
                    PDNA process was first proposed to be a “quick lessons
                    learned” exercise in November 2015. The present review was
                    conducted by the UNDP and World Bank’s Global Facility for
                    Disaster Reduction and Recovery (GFDRR) with support from
                    the EU from March–June 2017.
                  </p>
                  <p>
                    <a
                      href="https://reliefweb.int/report/world/post-disaster-needs-assessment-pdna-lessons-decade-experience-2018#:~:text=Rationale%2C%20approach%2C%20and%20general%20conclusions,through%20a%20government%2Dled%20process."
